/**
 * 指定された感情にマッチする語彙をランダムで取得する
 */
export function getEmotionWord(category: string): string {
    const list = EMOTION_VOCABULARY[category];
    if (!list) return '';
    return list[Math.floor(Math.random() * list.length)];
}

/**
 * 感情タイプを語彙カテゴリーに変換する
 */
export function mapEmotionToCategory(emotion: string): string {
    const e = emotion.toLowerCase();
    if (['joy', 'satisfied', 'satisfied', 'pride', 'proud'].includes(e)) return 'joy';
    if (['excited', 'expectant', 'anticipation', 'surprise'].includes(e)) return 'excited';
    if (['relieved', 'relief'].includes(e)) return 'relieved';
    if (['lonely', 'loneliness', 'clingy', 'affection'].includes(e)) return 'lonely';
    if (['anxious', 'anxiety', 'confusion'].includes(e)) return 'anxious';
    if (['jealous', 'jealousy'].includes(e)) return 'jealous';
    if (['angry', 'anger'].includes(e)) return 'angry';
    if (['tired'].includes(e)) return 'tired';
    return 'joy';
}
